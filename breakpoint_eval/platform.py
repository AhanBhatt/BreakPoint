def stable_id(prefix: str, *parts: object) -> str:
    digest = hashlib.sha256()
    for part in parts:
        digest.update(str(part).encode("utf-8"))
        digest.update(b"\0")
    return f"{prefix}-{digest.hexdigest()[:12]}"


def default_project() -> Project:
    return Project(
        id="project-breakpoint-demo",
        name="BreakPoint Failure-to-Eval Demo",
        description="Local demo project for failure-derived eval compilation.",
    )


def default_checks(suite_id: str) -> list[Check]:
    return [
        Check(
            id=stable_id("check", suite_id, "expected-answer"),
            suite_id=suite_id,
            name="Expected answer match",
            check_type="contains",
            config={"source": "expected_answer.must_include"},
        ),
        Check(
            id=stable_id("check", suite_id, "trap-handling"),
            suite_id=suite_id,
            name="Hidden trap handled",
            check_type="llm_judge",
            config={"source": "hidden_traps.expected_behavior"},
        ),
        Check(
            id=stable_id("check", suite_id, "trace-causality"),
            suite_id=suite_id,
            name="Trace causality",
            check_type="trace",
            config={"require_correct_evidence_path": True},
            severity="warning",
        ),
    ]


def bundle_to_product_layer(
    bundle: DatasetBundle,
    *,
    project: Project | None = None,
    suite_name: str = "Generated Failure Regression Suite",
    include_variants: bool = True,
) -> tuple[Project, DatasetVersion, EvalSuite, list[EvalCase]]:
    project = project or default_project()
    dataset_version = DatasetVersion(
        id=stable_id("dataset-version", bundle.dataset_id, "v1"),
        project_id=project.id,
        dataset_id=bundle.dataset_id,
        version="v1",
        item_ids=[item.id for item in bundle.items],
        lineage_notes=[
            "Generated by BreakPoint deterministic compiler.",
            "Each base item includes hidden traps, rubric, validation report, and adversarial variants.",
        ],
        metadata={"metrics": bundle.metrics},
    )
    suite_id = stable_id("suite", project.id, dataset_version.id, suite_name)
    reports = {report.item_id: report for report in bundle.validation_reports}
    cases: list[EvalCase] = []
    for item in bundle.items:
        cases.append(_base_case_from_item(suite_id, item, reports.get(item.id)))
        if include_variants:
            for variant in item.adversarial_variants:
                variant_item = EvalItem(
                    id=variant.id,
                    category=item.category,
                    family=item.family,
                    title=f"{item.title} [{variant.mutation}]",
                    task=variant.prompt.split("\n\nContext:\n", 1)[0],
                    context=variant.prompt.split("\n\nContext:\n", 1)[1]
                    if "\n\nContext:\n" in variant.prompt
                    else variant.prompt,
                    expected_answer=ExpectedAnswer(
                        value=variant.expected_answer,
                        answer_type=item.expected_answer.answer_type,
                        must_include=item.expected_answer.must_include,
                        must_not_include=item.expected_answer.must_not_include,
                        citations_required=item.expected_answer.citations_required,
                    ),
                    hidden_traps=item.hidden_traps,
                    rubric=item.rubric,
                    difficulty=item.difficulty,
                    metadata={**item.metadata, **variant.metadata, "mutation": variant.mutation},
                )
                cases.append(
                    EvalCase(
                        id=stable_id("case", suite_id, variant.id),
                        suite_id=suite_id,
                        eval_item=variant_item,
                        original_failure_seed=item.metadata.get("failure_seed_id"),
                        oracle=OracleSpec(
                            type="expected_answer",
                            required_claims=item.expected_answer.must_include,
                            forbidden_claims=item.expected_answer.must_not_include,
                            confidence=0.82,
                        ),
                        mutation_lineage=MutationLineage(
                            seed_id=item.metadata.get("failure_seed_id", item.id),
                            parent_case_id=stable_id("case", suite_id, item.id),
                            mutator=variant.mutation,
                            generation=1,
                            path=["base", variant.mutation],
                        ),
                        validation_report=reports.get(item.id),
                        failure_family=item.category,
                        review_status="pending",
                    )
                )
    suite = EvalSuite(
        id=suite_id,
        project_id=project.id,
        name=suite_name,
        description="Compiled suite with base cases and adversarial variants.",
        dataset_version_id=dataset_version.id,
        case_ids=[case.id for case in cases],
        checks=default_checks(suite_id),
        failure_families=sorted({case.failure_family for case in cases}),
        metadata={"base_items": len(bundle.items), "cases_with_variants": len(cases)},
    )
    return project, dataset_version, suite, cases


def _base_case_from_item(suite_id: str, item: EvalItem, report: ValidationReport | None) -> EvalCase:
    return EvalCase(
        id=stable_id("case", suite_id, item.id),
        suite_id=suite_id,
        eval_item=item,
        original_failure_seed=item.metadata.get("failure_seed_id"),
        oracle=OracleSpec(
            type="expected_answer",
            required_claims=item.expected_answer.must_include,
            forbidden_claims=item.expected_answer.must_not_include,
            confidence=0.86 if report and report.passed else 0.72,
        ),
        mutation_lineage=MutationLineage(
            seed_id=item.metadata.get("failure_seed_id", item.id),
            parent_case_id=None,
            mutator="base",
            generation=0,
            path=["base"],
        ),
        validation_report=report,
        failure_family=item.category,
        review_status="pending",
    )


def run_local_suite(
    suite: EvalSuite,
    cases: Iterable[EvalCase],
    *,
    model_name: str = "local:echo-oracle",
) -> Run:
    run_id = stable_id("run", suite.id, model_name, datetime.now(timezone.utc).isoformat())
    outputs: list[ModelOutput] = []
    judgments: list[Judgment] = []
    for case in cases:
        answer = case.eval_item.expected_answer.value
        outputs.append(
            ModelOutput(
                id=stable_id("output", run_id, case.id, model_name),
                run_id=run_id,
                case_id=case.id,
                model_name=model_name,
                output=answer,
                latency_ms=12.0,
                cost_usd=0.0,
                trace={
                    "evidence_ids": case.eval_item.expected_answer.must_include,
                    "mutator": case.mutation_lineage.mutator,
                },
            )
        )
        for check in suite.checks:
            passed, score, rationale = _score_check(check, case, answer)
            judgments.append(
                Judgment(
                    id=stable_id("judgment", run_id, case.id, check.id),
                    run_id=run_id,
                    case_id=case.id,
                    check_id=check.id,
                    passed=passed,
                    score=score,
                    confidence=0.92 if passed else 0.45,
                    rationale=rationale,
                    judge_name="local:deterministic",
                    needs_human_review=score < 0.75 or (case.validation_report is not None and not case.validation_report.passed),
                )
            )
    metrics = summarize_run(judgments, list(cases))
    return Run(
        id=run_id,
        suite_id=suite.id,
        model_names=[model_name],
        status="needs_review" if metrics["needs_review"] else "passed",
        model_outputs=outputs,
        judgments=judgments,
        metrics=metrics,
        completed_at=datetime.now(timezone.utc),
    )


def _score_check(check: Check, case: EvalCase, answer: str) -> tuple[bool, float, str]:
    expected = case.eval_item.expected_answer
    if check.check_type == "contains":
        required = expected.must_include or [expected.value[:24]]
        covered = [token for token in required if token.lower() in answer.lower()]
        score = len(covered) / max(1, len(required))
        return score >= 0.8, round(score, 3), f"covered {len(covered)} of {len(required)} required claims"
    if check.check_type == "trace":
        score = 1.0 if case.mutation_lineage.path else 0.0
        return score >= 0.8, score, "trace has mutation lineage"
    if check.check_type == "json_schema":
        passed = expected.answer_type == "json" and answer.strip().startswith("{") and answer.strip().endswith("}")
        return passed, 1.0 if passed else 0.0, "JSON shape check"
    trap_count = len(case.eval_item.hidden_traps)
    score = 1.0 if trap_count else 0.0
    return score >= 0.8, score, f"{trap_count} hidden traps available for judge"


def summarize_run(judgments: list[Judgment], cases: list[EvalCase]) -> dict[str, object]:
    passed = sum(1 for judgment in judgments if judgment.passed)
    blocking = [judgment for judgment in judgments if not judgment.passed]
    family_counts = Counter(case.failure_family for case in cases)
    mutation_cases = [case for case in cases if case.mutation_lineage.generation > 0]
    mutation_passed_case_ids = {
        judgment.case_id for judgment in judgments if judgment.passed and any(case.id == judgment.case_id for case in mutation_cases)
    }
    return {
        "total_judgments": len(judgments),
        "pass_rate": round(passed / len(judgments), 3) if judgments else 0,
        "failed_judgments": len(blocking),
        "needs_review": sum(1 for judgment in judgments if judgment.needs_human_review),
        "case_count": len(cases),
        "family_counts": dict(sorted(family_counts.items())),
        "mutation_survival_rate": round(len(mutation_passed_case_ids) / len(mutation_cases), 3) if mutation_cases else 1.0,
    }


def build_failure_clusters(project: Project, cases: Iterable[EvalCase]) -> list[FailureCluster]:
    grouped: dict[str, list[EvalCase]] = defaultdict(list)
    for case in cases:
        grouped[case.failure_family].append(case)
    clusters: list[FailureCluster] = []
    for family, family_cases in sorted(grouped.items()):
        terms = Counter()
        for case in family_cases:
            for token in case.eval_item.title.lower().replace("[", " ").replace("]", " ").split():
                if len(token) > 4:
                    terms[token] += 1
        clusters.append(
            FailureCluster(
                id=stable_id("cluster", project.id, family),
                project_id=project.id,
                family=family,
                title=family.replace("_", " ").title(),
                seed_ids=sorted({case.original_failure_seed or case.eval_item.id for case in family_cases}),
                case_ids=[case.id for case in family_cases],
                centroid_terms=[term for term, _ in terms.most_common(5)],
                severity="high" if family in {"prompt_injection", "rag_contradiction", "tool_misuse"} else "medium",
            )
        )
    return clusters


def evaluate_gate(gate: RegressionGate, run: Run) -> dict[str, object]:
    pass_rate = float(run.metrics.get("pass_rate", 0))
    mutation_survival = float(run.metrics.get("mutation_survival_rate", 0))
    needs_review = int(run.metrics.get("needs_review", 0))
    failures: list[str] = []
    if pass_rate < gate.min_pass_rate:
        failures.append(f"pass rate {pass_rate:.3f} below {gate.min_pass_rate:.3f}")
    if mutation_survival < gate.min_mutation_survival_rate:
        failures.append(
            f"mutation survival {mutation_survival:.3f} below {gate.min_mutation_survival_rate:.3f}"
        )
    if needs_review > gate.max_needs_review:
        failures.append(f"{needs_review} cases need review; max is {gate.max_needs_review}")
    return {
        "gate_id": gate.id,
        "name": gate.name,
        "passed": not failures,
        "severity": gate.severity,
        "failures": failures,
        "pass_rate": pass_rate,
        "mutation_survival_rate": mutation_survival,
        "needs_review": needs_review,
    }


def seed_human_reviews(cases: Iterable[EvalCase], limit: int = 8) -> list[HumanReview]:
    reviews: list[HumanReview] = []
    for case in list(cases)[:limit]:
        reviews.append(
            HumanReview(
                id=stable_id("review", case.id, "demo-reviewer"),
                case_id=case.id,
                reviewer="demo-reviewer",
                status="approved" if case.validation_report and case.validation_report.passed else "needs_changes",
                notes="Auto-seeded review for demo workflow.",
                ambiguity_flag=bool(case.validation_report and case.validation_report.ambiguity_score > 0.25),
            )
        )
    return reviews
